    private void btnSaveNasabahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveNasabahActionPerformed
        try {
            if (txtAlamatNasabah.getText().isEmpty() || txtBonus.getText().isEmpty() || txtHome.getText().isEmpty() || txtNamaNasabah.getText().isEmpty() || txtPenghasilan.getText().isEmpty() || txtPenghasilanTambahan.getText().isEmpty() || txtPhone.getText().isEmpty() || txtTanggungan.getText().isEmpty() || cbStatus.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "ERROR DATA NASABAH BELUM LENGKAP", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {

                n.setAlamat(txtAlamatNasabah.getText());
                n.setBonus(Double.valueOf(txtBonus.getText()));
                n.setHome(txtHome.getText());
                n.setJumlahTanggungan(Integer.valueOf(txtTanggungan.getText()));
                n.setNamaNasabah(txtNamaNasabah.getText());
                n.setPenghasilan(Double.valueOf(txtPenghasilan.getText()));
                n.setPenghasilanTambahan(Double.valueOf(txtPenghasilanTambahan.getText()));
                n.setPhone(txtPhone.getText());
                n.setStatus(cbStatus.getSelectedItem().toString());
                n.setPenghasilanTotal(n.calculateTotalPenghasilan(n.getPenghasilanTambahan(), n.getPenghasilan(), n.getBonus()));
                txtPenghasilanTotal.setText(String.valueOf(n.getPenghasilanTotal()));
                setDisable();
                btnLnjt.setEnabled(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR DATA NASABAH BELUM LENGKAP", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnSaveNasabahActionPerformed

