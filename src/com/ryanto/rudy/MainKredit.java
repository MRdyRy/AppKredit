    private void btnAjukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjukanActionPerformed
        

        try {
            Double uangMuka = Double.valueOf(txtUangMuka.getText()), harga = Double.valueOf(txtHargaBarang.getText());
            if ((uangMuka > harga * 0.2) && (uangMuka < harga)) {
                dp.setDurasi(Integer.valueOf(txtDurasi.getText()));
                dp.setHargaBarang(Double.valueOf(txtHargaBarang.getText()));
                dp.setSukuBunga(Double.valueOf(txtSukuBunga.getText()));
                dp.setUangMuka(Double.valueOf(txtUangMuka.getText()));
                dp.setBungaCicilan(dp.calculateBungaCicilan());
                dp.setPokokCicilan(dp.calculatePokokCicilan());
                dp.setTotalKredit(dp.calculatetotalKredit());
                dp.setCicilanPerbulan(dp.calculateCicilanPerBulan());
                //setDisabledKredit();
                double limit = dp.calculatelimitPinjaman(n.getPenghasilan(), n.getJumlahTanggungan());
                double cicilanPerBln = dp.calculateCicilanPerBulan();

                if (limit > cicilanPerBln) {
                    labelTotalKredit.setText(String.valueOf(df.format(dp.getTotalKredit())));
                    labelUangMuka.setText(String.valueOf(df.format(dp.getUangMuka())));
                    labelStatus.setText("Disetujui");
                    labelBungaCicilan.setText(String.valueOf(df.format(dp.getBungaCicilan())));
                    labelCicilanPerbulan.setText(String.valueOf(df.format(cicilanPerBln)));
                    labelPokokCicilan.setText(String.valueOf(df.format(dp.getPokokCicilan())));
                    labelDurasi.setText(String.valueOf(df.format(dp.getDurasi())));
                    labelLimit.setText(String.valueOf(df.format(limit)));

                } else {
                    labelTotalKredit.setText(String.valueOf(df.format(dp.getTotalKredit())));
                    labelUangMuka.setText(String.valueOf(df.format(dp.getUangMuka())));
                    labelStatus.setText("Tidak Disetujui");
                    labelBungaCicilan.setText(String.valueOf(df.format(dp.getBungaCicilan())));
                    labelCicilanPerbulan.setText(String.valueOf(df.format(cicilanPerBln)));
                    labelPokokCicilan.setText(String.valueOf(df.format(dp.getPokokCicilan())));
                    labelDurasi.setText(String.valueOf(df.format(dp.getDurasi())));
                    labelLimit.setText(String.valueOf(df.format(limit)));
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "ERROR UANG MUKA MINIMAL 20% DARI HARGA BARANG");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR DATA PENGAJUAN MASIH ADA YANG KOSONG!!", "ERROR", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnAjukanActionPerformed

